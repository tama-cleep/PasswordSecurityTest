print("##############")
celcius = float(input("Masukan Suhu: "))
print(f"Suhu adalah {celcius} celcius")

# Reamur
Reamur = (4/5) * celcius
print(f"Konvrensi suhu dari celcius ke fahrenheit sekitar : {Reamur} Reamur")

# fahrenheit
fahrenheit = (9/5) * (celcius) + 32
print(f"Konvrensi suhu dari celcius ke fahrenheit sekitar : {fahrenheit} Farenheit")

# Kelvin
Kelvin = celcius + 273
print(f"Konvrensi suhu dari celcius ke kelvin sekitar : {Kelvin} Kelvin")